public class helloworld {
  /*
   * This is a helloworld programme. public表示main方法是公有的，任何场合都可以被引用。
   * static表示main方法静态的，不依赖类的对象的，在类加载时main()方法也会被加载进内存并一直存在。 void表示main方法返回值为空。
   * String[] arg或者String arg[]表示main方法的参数为一个字符串数组。
   */
  public static void main(String args[]) {
    System.out.println("helloworld!\n");
  }
}